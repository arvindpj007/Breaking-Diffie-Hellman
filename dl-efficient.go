// missingParametersError prints usage information when the program is invoked without the required input-file argument.
func missingParametersError() {
	fmt.Println("ERROR: Parameters missing!")
	fmt.Println("HELP:")
	fmt.Println("./dl-efficient <filename for input>")
}

// setupCLI validates the command-line arguments and returns the input filename.
func setupCLI() string {
	if len(os.Args) < 2 {
		missingParametersError()
		os.Exit(1)
	}
	inputFile := os.Args[1]
	return inputFile
}

